CREATE TABLE movies (
    id INTEGER PRIMARY KEY,
    collection TEXT,
    budget INTEGER,
    genres TEXT,
    imdb_id TEXT,
    original_language TEXT,
    overview TEXT,
    popularity REAL,
    production_companies TEXT,
    production_countries TEXT,
    release_date TEXT,
    revenue INTEGER,
    runtime REAL,
    spoken_languages TEXT,
    title TEXT,
    vote_average REAL,
    vote_count INTEGER
);